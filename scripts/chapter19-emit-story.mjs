function buildBody(lines110) {
  return `\n\n${lines110.join("\n")}\n`;
}

function assembleFromFinnish(fiBody) {
  const fiNorm = fiBody.replace(/\r\n/g, "\n");
  const fiTok = fiNorm.split(" ");
  const feTok = fiTok.map((t) => fakeEnglishFromFinnishToken(t));
  const sfTok = fiTok.map((t) => sfToken(t));
  return {
    storyFakeEnglish: feTok.join(" "),
    storySpokenFinnish: sfTok.join(" "),
    fiCount: fiTok.length,
    feCount: feTok.length,
    sfCount: sfTok.length,
  };
}


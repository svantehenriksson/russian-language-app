function fakeEnglishFromFinnishToken(t) {
  if (t === "\n") return "\n";
  const m = /^(\n*)([\s\S]*)$/.exec(t);
  const nl = m[1];
  const c = m[2];
  if (c.startsWith("=")) {
    const rest = c.slice(1);
    const g = EQ[rest];
    return nl + (g ?? `-${rest}`);
  }
  if (c.startsWith("-")) return nl + (SUF[c] ?? c);
  return nl + (LEX[c] ?? c);
}

function sfToken(raw) {
  if (raw === "\n") return "\n";
  const m = /^(\n*)([\s\S]*)$/.exec(raw);
  const nl = m[1];
  const w = m[2];
  const SPOKEN = {
    Luca: "Luka",
    hän: "se",
    Hän: "Se",
    ole: "oo",
    mutta: "mut",
    Mutta: "Mut",
    sitten: "sit",
    Sitten: "Sit",
    takaisin: "takas",
  };
  if (w.startsWith("-") || w.startsWith("=")) return nl + w;
  return nl + (SPOKEN[w] ?? w);
}

